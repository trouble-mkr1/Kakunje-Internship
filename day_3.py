'''
Task 7: [50, "apple", True, "car", 40.5]
1) Find length of list
2) replace True with False
3) [50, "kiwi", "boat", 20, "car", 40.5]
4) [5000, "kiwi", "boat", 20, "car", 40.5]
5) ["kiwi", "boat", 20, "car", 40.5] using remove()
6) ["kiwi", 20, "car", 40.5] using pop()
7) ["kiwi", 20, "car"] using del
8) ["kiwi", 20, "car", 100]
9) ["banana", "kiwi", 20, "car", 100]
10) ["banana", "kiwi", 20, 30.5, "car", 100]
11) []
'''
list = [50, "apple", True, "car", 40.5]
print("task 7 output\n")

print(len(list)) 
list[2] = False 
print(list)
list[1:3] = ["kiwi", "boat", 20] #[50, "kiwi", "boat", 20, "car", 40.5]
print(list)
list[0] = 5000 #[5000, "kiwi", "boat", 20, "car", 40.5]
print(list)
list.remove(5000) #["kiwi", "boat", 20, "car", 40.5]
print(list)
list.pop(1) #["kiwi", 20, "car", 40.5]
print(list)
del list[-1] #["kiwi", 20, "car"]
print(list)
list.append(100) #["kiwi", 20, "car", 100]
print(list)
list.insert(0, "banana") #["banana", "kiwi", 20, "car", 100]
print(list)
list.insert(3, 30.5) #["banana", "kiwi", 20, 30.5, "car", 100]
print(list)
list.clear() #[]
print(list)

print("==============================================================================================")
