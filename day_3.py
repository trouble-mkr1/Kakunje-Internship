'''
Task 6: ["Cat", "Dog", "Lion", "Tiger", "Rabbit", "Monkey"]
1) ["lion"]
2) ["Monkey", "Rabbit"]
3) ["Tiger", "Lion", "Dog"]
4) ["Cat", "Tiger"]
5) ["Tiger", "Cat"]
6) ["Monkey", "Lion"]
7) ["Rabbit", "Lion", "Cat"]
8) ["Monkey", "Rabbit", "Tiger", "Lion", "Dog", "Cat"]
'''
list = ["Cat", "Dog", "Lion", "Tiger", "Rabbit", "Monkey"]
print("task 6 output\n")

print(list[2]) #["lion"]
print(list[-1:-3:-1]) #["Monkey", "Rabbit"]
print(list[3:0:-1]) #["Tiger", "Lion", "Dog"]
print(list[0::3]) #["Cat", "Tiger"]
print(list[3::-3]) #["Tiger", "Cat"]
print(list[-1:-5:-3]) #["Monkey", "Lion"]
print(list[-2::-2]) #["Rabbit", "Lion", "Cat"]
list.reverse()
print(list) #["Monkey", "Rabbit", "Tiger", "Lion", "Dog", "Cat"]

